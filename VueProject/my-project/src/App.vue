import Home from './components/Home.vue';
import Modal from './components/ModalHomeTop.vue';

export default {
  name: 'App',
  components: {
    Modal,
    Home,
  },
  data() {
    return {
      isModalVisible: false,
    };
  },
  methods: {
    showModal() {
      this.isModalVisible = true;
    },
    closeModal() {
      this.isModalVisible = false;
    }
  }
};
</script>

